    <script>
        function openModal() {
            document.getElementById('myModal').style.display = "block";
        }

        function closeModal() {
            document.getElementById('myModal').style.display = "none";
        }

        let slideIndex = 1;
        showSlides(slideIndex);

        function plusSlides(n) {
            showSlides(slideIndex += n);
        }

        function currentSlide(n) {
            showSlides(slideIndex = n);
        }

        function showSlides(n) {
            let i;
            let imgs = document.getElementsByClassName("demo");
            let newSlide = document.getElementById("slide-img");
            if (n > imgs.length) {slideIndex = 1}
            if (n < 1) {slideIndex = imgs.length}

            newSlide.innerHTML = '<div class="numbertext">' + slideIndex + ' / 40</div>';
            newSlide.innerHTML += '<img src="' + imgs[slideIndex-1].src + '" style="width:100%" alt="' + imgs[slideIndex-1].alt +'">';
            newSlide.style.display = "block";
            document.getElementById("caption").innerHTML = imgs[slideIndex-1].alt;
        }
    </script>

